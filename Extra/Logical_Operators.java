public class Logical_Operators {
    public static void main(String[] args) {
        // Logical operators
        // &&
        int a = 30, b = 60;
        if(a < 50 && b < 50)
            System.out.println("Both are less than 50!");
        else
            System.out.println("May be one or both are bigger!");

        // ||
        if(a < 50 || b < 50)
            System.out.println("Both are less than 50!");
        else
            System.out.println("May be one or both are bigger!");

        // !
        

    }
}
